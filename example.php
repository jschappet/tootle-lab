<h2>Have you ever wondered what aspirin really does? </h2>

 

<p>Aspirin is one of the oldest drugs - yet we still know very little about how it functions. We know that aspirin blocks the synthesis of prostaglandins, which are lipid signaling molecules.</p>

 
<p>
Aspirin has many effects:</p>
<ul>
 <li>   Relief of inflammation and pain</li>

 <li>   Stomach irritation - bleeding ulcers</li>

  <li>  Reversible female infertility</li>

 <li>   Cancer prevention</li>

 <li>   Heart disease prevention</li>
</ul>


<p>It is unclear how aspirin causes these diverse effects, raising the question of what are the prostaglandins really doing?</p>

